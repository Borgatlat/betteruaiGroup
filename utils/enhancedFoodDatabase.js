// Enhanced detection algorithm
export const findBestFoodMatch = (description) => {
  const words = description.toLowerCase().split(/\s+/);
  let bestMatch = null;
  let bestScore = 0;

  // First, check for exact matches in synonyms
  for (const [synonym, foodName] of Object.entries(FOOD_SYNONYMS)) {
    if (description.toLowerCase().includes(synonym)) {
      const nutrition = ENHANCED_FOOD_DATABASE[foodName];
      if (nutrition) {
        return { name: foodName, ...nutrition };
      }
    }
  }

  // Then check the main database with enhanced scoring
  for (const [foodName, nutrition] of Object.entries(ENHANCED_FOOD_DATABASE)) {
    let score = 0;
    const foodWords = foodName.split(/\s+/);
    
    // Exact word matches (highest score)
    for (const word of words) {
      for (const foodWord of foodWords) {
        if (word === foodWord) {
          score += 10;
        } else if (word.includes(foodWord) || foodWord.includes(word)) {
          score += 5;
        }
      }
    }
    
    // Category-based scoring
    const category = nutrition.category;
    if (category === 'protein' && (words.includes('meat') || words.includes('protein'))) {
      score += 3;
    } else if (category === 'vegetable' && (words.includes('vegetable') || words.includes('veggie'))) {
      score += 3;
    } else if (category === 'fruit' && words.includes('fruit')) {
      score += 3;
    } else if (category === 'grain' && (words.includes('grain') || words.includes('carb'))) {
      score += 3;
    }
    
    // Cooking method scoring
    if (words.includes('grilled') && category === 'protein') {
      score += 2;
    } else if (words.includes('fried') && (category === 'protein' || category === 'fast_food')) {
      score += 2;
    } else if (words.includes('baked') && (category === 'dessert' || category === 'grain')) {
      score += 2;
    }
    
    // Portion size indicators
    if (words.includes('slice') && (foodName.includes('pizza') || foodName.includes('bread'))) {
      score += 2;
    } else if (words.includes('piece') && category === 'fruit') {
      score += 2;
    }
    
    if (score > bestScore) {
      bestScore = score;
      bestMatch = { name: foodName, ...nutrition };
    }
  }

  return bestMatch;
}; 